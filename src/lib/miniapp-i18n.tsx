// Merge PROMO_EXT + RESUB_EXT + WITHDRAW_EXT into main dicts.
(Object.keys(DICTS) as MiniAppLang[]).forEach((l) => {
  Object.assign(DICTS[l], PROMO_EXT[l], RESUB_EXT[l], WITHDRAW_EXT[l]);
});


const STORAGE_KEY = "miniapp-lang";

interface Ctx {
  lang: MiniAppLang;
  setLang: (l: MiniAppLang) => void;
  t: (key: string, vars?: Record<string, string | number>) => string;
}

const MiniAppI18nContext = createContext<Ctx>({
  lang: "ru",
  setLang: () => {},
  t: (k) => k,
});

export function MiniAppI18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<MiniAppLang>(() => {
    try {
      const saved = localStorage.getItem(STORAGE_KEY) as MiniAppLang | null;
      if (saved && DICTS[saved]) return saved;
    } catch {}
    return "ru";
  });

  useEffect(() => {
    try { localStorage.setItem(STORAGE_KEY, lang); } catch {}
  }, [lang]);

  const setLang = useCallback((l: MiniAppLang) => setLangState(l), []);

  const t = useCallback((key: string, vars?: Record<string, string | number>) => {
    const dict = DICTS[lang] || ru;
    let s = dict[key] ?? ru[key] ?? key;
    if (vars) {
      for (const k in vars) s = s.replace(new RegExp(`\\{${k}\\}`, "g"), String(vars[k]));
    }
    return s;
  }, [lang]);

  const value = useMemo(() => ({ lang, setLang, t }), [lang, setLang, t]);
  return <MiniAppI18nContext.Provider value={value}>{children}</MiniAppI18nContext.Provider>;
}

export function useMiniAppI18n() {
  return useContext(MiniAppI18nContext);
}
